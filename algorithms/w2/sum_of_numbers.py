n = int(input())

temp_sum = n
numbers = []
for x in range(1, n):
    if x < (temp_sum-x):
        numbers.append(x)
        temp_sum -= x
    elif x > (temp_sum - x):
        numbers.append(temp_sum)
        break

if len(numbers) == 0:
    numbers.append(n)

print(len(numbers))
for x in numbers:
    print(x, end=' ')

