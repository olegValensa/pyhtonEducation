def binarySearch(A, k):
    l = 0
    r = len(A)-1
    while l <= r:
        m = (l+r)//2
        if A[m] == k:
            return m+1
        elif A[m] > k:
            r = m - 1
        else:
            l = m + 1
    return -1

n = input().split()
k = input().split()
A = []
B = []

for i in n[1:]:
    A.append(int(i))
for i in k[1:]:
    B.append(int(i))

for x in B:
    print(binarySearch(A, x), end=' ')
